/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C1_Init();
  MX_I2S3_Init();
  MX_SPI1_Init();
  MX_USB_HOST_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_ADC2_Init();
  /* USER CODE BEGIN 2 */
  HAL_ADC_Start_DMA(&hadc2, (uint32_t*)adc_bufy, ADC_BUF_LENy);
  HAL_ADC_Start_DMA(&hadc2, (uint32_t*)adc_cs, ADC_BUF_CSV);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
  HAL_TIM_Encoder_Start(&htim3, TIM_CHANNEL_ALL);
  HAL_GPIO_WritePin(direction_GPIO_Port, direction_Pin, GPIO_PIN_SET);
  HAL_GPIO_WritePin(direction_GPIO_Port, direction_Pin, GPIO_PIN_RESET);
  EMA_Init(&filter, 0.5f);
 // HAL_GPIO_ReadPin(B1_GPIO_Port, B1_Pin);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    EMA_SetCoefficient(&filter, 0.5f);
	  HAL_TIM_PeriodElapsedCallback(&htim3);
	    HAL_TIM_IC_CaptureCallback(&htim3);
		/*De 42000000 is de klok snelheid van het systeem. De 42-1 is de prescaler.
		 *Door deze twee te delen krijg je de operating freq
		 *Het resultaat vermedifuldig je met 60.
		 *Dat resultaat deel je door de maximale RPM van de motor.
		 *Zo wordt er berekend hoeveel pulsen er nodig zijn voor 1 rotatie
		 * */

	rondje =((clock_speed /(prescaler) * 60)/max_rpm); // huidig 2048
	rondje2 =(-(clock_speed /(prescaler) * 60)/max_rpm);
		/*De formule hieronder wordt gebruikt om de benodigde rpm te berekenen voor 1 rondje.
		 *Dit wordt gedaan door de pulsen per seconde te vermenigvuldigen  met 60.
		 *Waarna je dat getal deelt door het aantal pulsen dat nodig is voor 1 rotatie.
		 *Het resultaat hiervan vermenigvuldigen  je daarna nog met de geat ratio van je motor
		 * */
	   RPM = (20000 * 60/ rondje * gear_ratio);//huidig 8190

	   mVoltage= adc_cs[1000];

	   /* hier filter ik de waardes van de joystick */
	   inputy = adc_bufy[1000];
	   float float_intputy = (float)inputy;
	   filter_inputy = EMA_Update(&filter, float_intputy);
	   int int_gefilterde_input = (int)filter_inputy;
	   gefilterde_inputy = (int32_t)  int_gefilterde_input;

     /*waardes die verdre in de code gebruikt worden */
	   int16_t Range = 10000;
	   int16_t ruiswaarde = 50;
	   int max_rotatie=(6 * rondje);//14336
	   int min_rotatie=(-6 * rondje);//-14336

	   /*hier worden de input en encoder positie aan elkaar gemapt*/
	   Gewilde_positie = MAP(inputy, 0, 4096, 0, Range);
	   huidige_positie = MAP(encoder_position, min_rotatie, max_rotatie, 0, Range);
	   verschil_positie = abs(Gewilde_positie - huidige_positie);
	   verschill_input = input_delay - int_gefilterde_input;
	    check_delay++;

	  while(check_delay > 0){// zorgt ervoor dat het verschill in beweging kan worden gemeten
		  HAL_Delay(1000);
		  input_delay = int_gefilterde_input;
		  check_delay =0;
	  }

 if(encoder_position < min_rotatie|| encoder_position > max_rotatie){
	 /*De motor moet stoppen met bewegen als de if statment true is,
	  *Zo vorkom je schade aan de motor en rotor
	  * */

	 __HAL_TIM_SET_COMPARE(&htim2,TIM_CHANNEL_1,0);
	 HAL_Delay(rust);

	 /* de code hieronder bewaardt de maximale waarde voor een later gebruik.
	 * Ook revert hij de motor zodat hij weer zou kunnen bewegen.
	 */
	    static uint8_t first_time = 0;
	    if(!first_time)
	    {

	     if(encoder_position < min_rotatie){
	    		HAL_Delay(rust);
	    		holdmin_encoder = encoder_position;
	    		reset_encoder(&enc_instance);
	    		HAL_GPIO_WritePin(direction_GPIO_Port, direction_Pin, GPIO_PIN_RESET);
	    		__HAL_TIM_SET_COMPARE(&htim2,TIM_CHANNEL_1,RPM);
		 }
	 	 if( encoder_position > max_rotatie){
	 		 HAL_Delay(rust);
	 		 holdmax_encoder = encoder_position;
	 		 reset_encoder(&enc_instance);
	 		 HAL_GPIO_WritePin(direction_GPIO_Port, direction_Pin, GPIO_PIN_SET);
	 		 __HAL_TIM_SET_COMPARE(&htim2,TIM_CHANNEL_1,RPM);
	 	 }
	 	first_time = 1;
	    }
	 /*
	  * De code hieronder gaat over het checken of de input groot genoeg is om het te zien als een
	  * beweging, om zo te voorkomen dat de hand altijd beweegd
	  */
 	 	 }else if(verschil_positie >= ruiswaarde){

 	 		 /*
 	 		  * Als de input groot genoeg is kijkt de code pas of er een verschill zit tussen
 	 		  * de gewenste waarde en de huidgie waarde. Met dan het idee dat de hand gaat bewegen.
 	 		  */
  		  	  if(!(huidige_positie == Gewilde_positie)){

  		  		  __HAL_TIM_SET_COMPARE(&htim2,TIM_CHANNEL_1,RPM);//hier komt de p.i.d controller

  		  		  //deze code huid bij hoeveel keer de motor een rotatie gemaakt heeft
  		  		  if(input_delay < int_gefilterde_input){
  		  			  while(aantal <= 6 && aantal >= 0){ //de rotor kan maar 7 keer roteren
  		  				  aantal++;
  		  				  tellen1++;
  		  				  hoeveel_rondjes = tellen1 * rondje;
  		  				  HAL_Delay(1000);
  		  			  }
  		  			  aantal = 0;
  		  			  tellen1 = 0;
  		  		  }else if(input_delay > int_gefilterde_input){
  		  			  while(aantal2 >= -6 && aantal2 < 0){ //de rotor kan maar 7 keer roteren
  		  				  aantal2--;
  		  				  tellen2++;
  		  				  hoeveel_rondjes2 = tellen2 * rondje2;
  		  				  HAL_Delay(1000);
  		  			  }
  		  			  aantal2 = 0;
  		  			  tellen2 = 0;
  		  		  }
  		  	  } else {
  		  		__HAL_TIM_SET_COMPARE(&htim2,TIM_CHANNEL_1,0);
  		  	  }
	    	}
  }
    /* USER CODE END WHILE */
    MX_USB_HOST_Process();

    /* USER CODE BEGIN 3 */

  /* USER CODE END 3 */
}

